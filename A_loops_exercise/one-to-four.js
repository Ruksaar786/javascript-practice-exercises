//write a function `oneToFour` that prints all whole numbers from one to four, inclusive. the function
//takes in no arguments and doesnt need to return any value. it should just print to the terminal

const oneToFour = function () {
  for (let i = 1; i <= 4; i++) {
    console.log(i);
  }
};

oneToFour;
